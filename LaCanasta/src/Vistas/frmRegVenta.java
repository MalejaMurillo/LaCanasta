    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        int filaSele = tblProductos.getSelectedRow();
        if (filaSele < 0 || txtCantidad.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Verifique si selecciono un producto y/o digito la cantidad");
        } else {
            double total = Double.parseDouble(txtCantidad.getText()) * listaPr.get(filaSele).getValorUnitario();
            DefaultTableModel dtm = (DefaultTableModel) tblProductosA.getModel();

            dtm.addRow(new Object[]{listaPr.get(filaSele).getNombre(),
                txtCantidad.getText(),
                total

            });

            pr = pr.mtdObtenerObjetoPorId(listaPr.get(filaSele).getIdProducto());
            listaPrs.add(pr);

        }
    }//GEN-LAST:event_btnAgregarActionPerformed

