    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtNombres.getText().trim().equals("") || txtNombres.getText().trim().equals("") || txtNombres.getText().trim().equals("") || tblProductosA.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(this, "Verifique si ingreso los datos del cliente Ã³ si aÃ±adio producto a la venta");
        } else {
            cl = new Cliente(txtNombres.getText(), txtApellidos.getText(), txtDocumento.getText());

            int id = cl.mtdGuardar();
            cl = cl.mtdObtenerObjetoPorId(id);
            pg = (Programacion) cmbProgramacion.getSelectedItem();
            DateFormat format = new SimpleDateFormat("yyyy-mm-dd");
            try {
                venta.setFecha(format.parse(txtFecha.getText()));
            } catch (ParseException ex) {
                Logger.getLogger(frmRegVenta.class.getName()).log(Level.SEVERE, null, ex);
            }
            venta.setCliente(cl);
            venta.setProgramacion(pg);
            int idVenta = venta.mtdGuardar();
            venta = venta.mtdObtenerObjetoPorId(idVenta);

            for (int i = 0; i < listaPrs.size(); i++) {
                DefaultTableModel dtm = (DefaultTableModel) tblProductosA.getModel();
                Detalle detalle = new Detalle(venta, listaPrs.get(i), Integer.parseInt(dtm.getValueAt(i, 1).toString()), Double.parseDouble(dtm.getValueAt(i, 2).toString()));
                detalle.mtdGuardar();
                Producto pr = listaPrs.get(i);
                pr.setCantidad(pr.getCantidad() - Integer.parseInt(dtm.getValueAt(i, 1).toString()));
                pr.mtdActualizar();
            }
            JOptionPane.showMessageDialog(this, "Los datos se guardaron correctamente");

        }

    }//GEN-LAST:event_btnGuardarActionPerformed

